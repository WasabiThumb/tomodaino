
/**
 * Lookup table determining how traits map to personality types
 * created through painstaking data mining. The table is highly
 * bit-packed to save space and to avoid explicitly fetching
 * binary data.
 * @param n {number} A traits mask (0o0000 - 0o7777)
 * @return A personality value (0x0 - 0xF)
 */
const traitLut: (n: number) => number = ((b, I) => {
    const i = ((n: number) => (I[n >> 2] >> (6 - ((n & 3) << 1))) & 3);
    return ((index: number) => {
        if (index < 0o0000 || index > 0o7777)
            throw new Error(`trait LUT index ${index} is out of bounds`);
        const oi = index >> 1;
        const o = b[i(oi >> 5)][oi & 0x1F];
        return index & 1 ? o & 0xF : o >>> 4;
    });
})(
    // The full 2kB LUT, after analysis,
    // is composed of only 4 unique 32 byte blocks.
    [
        [
            0xAA, 0xAA, 0x88, 0x81, 0xAA, 0xA8, 0x88, 0x11, 0xAA, 0x88, 0x81, 0x11, 0xA8, 0x88, 0x11, 0x11,
            0x88, 0x88, 0x11, 0x10, 0x88, 0x81, 0x11, 0x00, 0x88, 0x11, 0x10, 0x00, 0x81, 0x11, 0x00, 0x00
        ],
        [
            0xBB, 0xBB, 0x99, 0x93, 0xBB, 0xB9, 0x99, 0x33, 0xBB, 0x99, 0x93, 0x33, 0xB9, 0x99, 0x33, 0x33,
            0x99, 0x99, 0x33, 0x32, 0x99, 0x93, 0x33, 0x22, 0x99, 0x33, 0x32, 0x22, 0x93, 0x33, 0x22, 0x22
        ],
        [
            0xEE, 0xEE, 0xCC, 0xC6, 0xEE, 0xEC, 0xCC, 0x66, 0xEE, 0xCC, 0xC6, 0x66, 0xEC, 0xCC, 0x66, 0x66,
            0xCC, 0xCC, 0x66, 0x64, 0xCC, 0xC6, 0x66, 0x44, 0xCC, 0x66, 0x64, 0x44, 0xC6, 0x66, 0x44, 0x44
        ],
        [
            0xFF, 0xFF, 0xDD, 0xD7, 0xFF, 0xFD, 0xDD, 0x77, 0xFF, 0xDD, 0xD7, 0x77, 0xFD, 0xDD, 0x77, 0x77,
            0xDD, 0xDD, 0x77, 0x75, 0xDD, 0xD7, 0x77, 0x55, 0xDD, 0x77, 0x75, 0x55, 0xD7, 0x77, 0x55, 0x55
        ]
    ].map((o) => new Uint8Array(o)),

    // Block order within the LUT.
    new Uint8Array([
        0x00, 0x56, 0x01, 0x5A,
        0x05, 0x6A, 0x15, 0xAA,
        0x55, 0xAB, 0x56, 0xAF,
        0x5A, 0xBF, 0x6A, 0xFF
    ])
);

